Natural C is a variant of the standard C programming language that emphasises
simplicity, concision, and reliability. These qualities can help programmers
to write more secure programs.

<p>
For more information, see the <a href="docs.php">documentation</a> pages,
or visit the <a href="lists.php">mailing list</a> to ask a question.

The <a href="download.php">source code</a> is available for download, and
everyone is free to check out a working copy of the <a href="svn.php">Subversion repository</a>.

